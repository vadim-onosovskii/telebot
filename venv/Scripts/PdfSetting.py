class PdfSetting:
    def __init__(self):
        self.font_family = ""
        self.font_size = 0
        self.alignment = ""

    def setFontFamily(self, font_family):
        self.font_family = font_family

    def setFontSize(self, font_size):
        self.font_size = font_size

    def setAlignment(self, alignment):
        self.alignment = alignment

